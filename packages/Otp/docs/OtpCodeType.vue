@lang:en
### Code type

It is possible to define the type of the code, based on the basic types of the HTML `input` tag.\
The two possible values are `text` and `number`.

The type `number` allows you to only enter numbers, and to display the numeric keypad on mobile.
@lang

@lang:fr
### Type du code

Il est possible de définir le type du code, en se basant sur les types de base de la balise HTML `<input>`.\
Les deux valeurs possible sont `text` et `number`.

Le type `number` permet de n'autoriser que la saisie de chiffres, et sur mobile d'afficher le pavé numérique.
@lang

